const LANG_NAMES = { de: "Deutsch", tr: "Türkçe", en: "English" };
let currentLang = "de";

/* ---------- Language switching ---------- */
function t(key) {
  return (I18N[currentLang] && I18N[currentLang][key]) || I18N.de[key] || key;
}

function applyLang(lang) {
  if (!I18N[lang]) lang = "de";
  currentLang = lang;
  document.documentElement.lang = lang;

  document.querySelectorAll("[data-i18n]").forEach((el) => {
    const key = el.getAttribute("data-i18n");
    const val = t(key);
    if (val == null) return;
    if (el.tagName === "META") {
      el.setAttribute("content", val);
    } else if (el.hasAttribute("data-i18n-attr")) {
      el.setAttribute(el.getAttribute("data-i18n-attr"), val);
    } else {
      el.textContent = val;
    }
  });

  document.querySelectorAll(".lang-switch button").forEach((btn) => {
    const active = btn.dataset.lang === lang;
    btn.classList.toggle("active", active);
    btn.setAttribute("aria-pressed", String(active));
  });

  try { localStorage.setItem("meva-lang", lang); } catch (e) {}
}

