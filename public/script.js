document.addEventListener("DOMContentLoaded", () => {
  // German is the default. Only switch if the visitor previously chose a language.
  let initial = "de";
  try {
    const saved = localStorage.getItem("meva-lang");
    if (saved && I18N[saved]) initial = saved;
  } catch (e) {}
  applyLang(initial);

  // Language buttons
  document.querySelectorAll(".lang-switch button").forEach((btn) => {
    btn.addEventListener("click", () => applyLang(btn.dataset.lang));
  });

  // Year
  const yearEl = document.getElementById("year");
  if (yearEl) yearEl.textContent = "2026";

  // Sticky header shadow
  const header = document.getElementById("siteHeader");
  const onScroll = () => header.classList.toggle("scrolled", window.scrollY > 8);
  onScroll();
  window.addEventListener("scroll", onScroll, { passive: true });

  // Mobile nav
  const nav = document.getElementById("mainNav");
  const toggle = document.getElementById("navToggle");
  const closeNav = () => {
    nav.classList.remove("open");
    toggle.classList.remove("active");
    toggle.setAttribute("aria-expanded", "false");
  };
  toggle.addEventListener("click", () => {
    const open = nav.classList.toggle("open");
    toggle.classList.toggle("active", open);
    toggle.setAttribute("aria-expanded", String(open));
  });
  nav.querySelectorAll("a").forEach((a) => a.addEventListener("click", closeNav));

  // Reveal on scroll
  const revealEls = document.querySelectorAll(
    ".feature-card, .service-card, .about-text, .about-media, .contact-info, .contact-form-wrap, .section-head"
  );
  revealEls.forEach((el) => el.classList.add("reveal"));
  if ("IntersectionObserver" in window) {
    const io = new IntersectionObserver(
      (entries) => {
        entries.forEach((entry) => {
          if (entry.isIntersecting) {
            entry.target.classList.add("in");
            io.unobserve(entry.target);
          }
        });
      },
      { threshold: 0.12, rootMargin: "0px 0px -40px 0px" }
    );
    revealEls.forEach((el) => io.observe(el));
  } else {
    revealEls.forEach((el) => el.classList.add("in"));
  }

  // Contact form → mailto
  const form = document.getElementById("offerForm");
  if (form) {
    form.addEventListener("submit", (e) => {
      e.preventDefault();
      const data = new FormData(form);
      const name = (data.get("name") || "").toString().trim();
      const email = (data.get("email") || "").toString().trim();
      const phone = (data.get("phone") || "").toString().trim();
      const service = (data.get("service") || "").toString().trim();
      const message = (data.get("message") || "").toString().trim();

      if (!name) {
        alert(t("form.alertName"));
        document.getElementById("f-name").focus();
        return;
      }

      const L = {
        de: { name: "Name / Firma", email: "E-Mail", phone: "Telefon", service: "Gewünschte Leistung", message: "Nachricht / Objektdetails" },
        tr: { name: "Ad / Firma", email: "E-posta", phone: "Telefon", service: "İstenen hizmet", message: "Mesaj / Nesne detayları" },
        en: { name: "Name / Company", email: "Email", phone: "Phone", service: "Desired service", message: "Message / Property details" }
      }[currentLang];

      const bodyLines = [
        `${L.name}: ${name}`,
        `${L.email}: ${email}`,
        `${L.phone}: ${phone}`,
        `${L.service}: ${service}`,
        "",
        `${L.message}:`,
        message
      ];

      const subject = encodeURIComponent(t("form.mailSubject") + (name ? " – " + name : ""));
      const body = encodeURIComponent(bodyLines.join("\n"));
      window.location.href = `mailto:mevagebaeudereinigung@gmail.com?subject=${subject}&body=${body}`;
    });
  }

});
